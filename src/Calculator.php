<?php
// Calculator.php

class Calculator {
    public function add($a, $b) {
        return $a + $b;
    }

    public function subtract($a, $b) {
        return $a - $b;
    }

    public function multiply($a, $b) {
        return $a * $b;
    }

    public function divide($a, $b) {
        return $a / $b;
    }
    public function sqrt($a) {
        return $a;
    }

    public function rand($a, $b) {
        return $a - $b;
    }

    public function min($a, $b) {
        return $a * $b;
    }

    public function max($a, $b) {
        return $a / $b;
    }
}